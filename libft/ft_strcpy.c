#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char	*original_dest;

	if (!dest || !src)
		return (NULL);
	original_dest = dest;
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (original_dest);
}
